package Basics;

public class assingment_op {
    public static void main(String[] args) {

        int a = 20;

        a += 5; // a = 20 + 5
        System.out.println(a); // 25

        a -= 3; // a = 25 - 3
        System.out.println(a); // 22

        a *= 2; // a = 22 * 2
        System.out.println(a); // 44

        a /= 4; // a = 44 / 4
        System.out.println(a); // 11

        a %= 3; // a = 11 % 3
        System.out.println(a); // 2

    }

}
