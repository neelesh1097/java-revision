package Basics;

public class relational_op {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b); // false
        System.out.println(a < b); // true
        System.out.println(a >= 10); // true
        System.out.println(b <= 15); // false

    }

}
